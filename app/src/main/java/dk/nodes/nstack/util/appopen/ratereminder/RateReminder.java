public class RateReminder {

    private String title;
    private String body;
    private String yesBtn;
    private String laterBtn;
    private String noBtn;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }

    public String getYesBtn() {
        return yesBtn;
    }

    public void setYesBtn(String yesBtn) {
        this.yesBtn = yesBtn;
    }

    public String getLaterBtn() {
        return laterBtn;
    }

    public void setLaterBtn(String laterBtn) {
        this.laterBtn = laterBtn;
    }

    public String getNoBtn() {
        return noBtn;
    }

    public void setNoBtn(String noBtn) {
        this.noBtn = noBtn;
    }
}
